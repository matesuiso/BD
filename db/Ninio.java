public class Ninio {
   String nombre;
   String apellido;
   String dni;
   String telefono;
   String fecha;

   public Ninio(String nombre, String apellido, String dni, String telefono, String fecha) {
      this.nombre = nombre;
      this.apellido = apellido;
      this.dni = dni;
      this.telefono = telefono;
      this.fecha = fecha;
   }




}
